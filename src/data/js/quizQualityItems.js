export const JS_QUIZ_QUALITY_ITEM_IDS = Object.freeze(
  Object.keys(JS_QUIZ_QUALITY_ITEMS),
);

export function applyJsQuizQualityItems(quizzes = []) {
  return quizzes.map((quiz) => {
    const qualityItem = JS_QUIZ_QUALITY_ITEMS[quiz.lessonId];
    if (!qualityItem) return quiz;

    const questions = Array.isArray(quiz.questions) ? quiz.questions : [];
    if (questions.some((question) => question?.id === qualityItem.id)) return quiz;

    return {
      ...quiz,
      questions: [...questions, qualityItem],
    };
  });
}
