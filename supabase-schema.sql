-- 1. User Profiles (extends Supabase auth.users)
create table if not exists public.profiles (
  id uuid references auth.users on delete cascade primary key,
  display_name text check (char_length(display_name) <= 60),
  avatar_url text check (char_length(avatar_url) <= 500),
  created_at timestamptz default now()
);

-- 2. Progress — completed lessons + quiz scores
create table if not exists public.progress (
  id uuid default gen_random_uuid() primary key,
  user_id uuid references auth.users on delete cascade not null,
  lesson_key text not null,
  completed_at timestamptz default now(),
  unique(user_id, lesson_key)
);

create table if not exists public.quiz_scores (
  id uuid default gen_random_uuid() primary key,
  user_id uuid references auth.users on delete cascade not null,
  quiz_key text not null,
  score text not null,
  completed_at timestamptz default now(),
  unique(user_id, quiz_key)
);

-- 3. XP
create table if not exists public.xp (
  user_id uuid references auth.users on delete cascade primary key,
  total integer default 0,
  updated_at timestamptz default now()
);

-- 4. Streaks
create table if not exists public.streaks (
  user_id uuid references auth.users on delete cascade primary key,
  days integer default 0,
  last_date date,
  updated_at timestamptz default now()
);

-- 5. Daily Goals
create table if not exists public.daily_goals (
  user_id uuid references auth.users on delete cascade primary key,
  goal_date date default current_date,
  count integer default 0,
  updated_at timestamptz default now()
);

-- 6. Earned Badges
create table if not exists public.badges (
  id uuid default gen_random_uuid() primary key,
  user_id uuid references auth.users on delete cascade not null,
  badge_id text not null,
  earned_at timestamptz default now(),
  unique(user_id, badge_id)
);

-- 7. Spaced Repetition Queue
create table if not exists public.sr_cards (
  id uuid default gen_random_uuid() primary key,
  user_id uuid references auth.users on delete cascade not null,
  question text not null,
  code text,
  options jsonb not null,
  correct integer not null,
  explanation text not null,
  source text not null,
  added_at timestamptz default now(),
  next_review timestamptz default now(),
  interval_days integer default 1,
  ease real default 2.5,
  unique(user_id, question)
);

-- 8. Bookmarks (NEW)
create table if not exists public.bookmarks (
  id uuid default gen_random_uuid() primary key,
  user_id uuid references auth.users on delete cascade not null,
  lesson_key text not null,
  course_id text not null,
  lesson_title text not null,
  created_at timestamptz default now(),
  unique(user_id, lesson_key)
);

-- 9. Notes (NEW)
create table if not exists public.notes (
  id uuid default gen_random_uuid() primary key,
  user_id uuid references auth.users on delete cascade not null,
  lesson_key text not null,
  content text not null check (char_length(content) <= 10000),
  updated_at timestamptz default now(),
  unique(user_id, lesson_key)
);

-- 10. Courses Visited (for Explorer badge)
create table if not exists public.courses_visited (
  id uuid default gen_random_uuid() primary key,
  user_id uuid references auth.users on delete cascade not null,
  course_id text not null,
  visited_at timestamptz default now(),
  unique(user_id, course_id)
);

-- 11. Last Position (resume where you left off)
create table if not exists public.last_position (
  user_id uuid references auth.users on delete cascade primary key,
  course text,
  mod text,
  les text,
  course_id text,
  module_id text,
  lesson_id text,
  is_module_quiz boolean default false,
  updated_at timestamptz default now()
);

-- 12. Product analytics events (onboarding, lesson flow, etc.)
create table if not exists public.analytics_events (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users on delete cascade not null,
  event_name text not null check (char_length(event_name) <= 80),
  path text not null default '/' check (char_length(path) <= 300),
  payload jsonb not null default '{}'::jsonb,
  source text not null default 'web',
  occurred_at timestamptz not null default now(),
  created_at timestamptz not null default now()
);

