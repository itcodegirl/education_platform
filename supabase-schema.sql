-- ═══════════════════════════════════════════════
-- ADMIN ESCALATION GUARD
-- ═══════════════════════════════════════════════
--
-- Problem: the "Admins update all profiles" policy above lets any admin
-- run `UPDATE profiles SET is_admin = true WHERE id = ...` on ANY row,
-- including their own. One compromised admin = permanent platform
-- takeover. Below we:
--   1. Block direct edits to the is_admin column from the REST API.
--   2. Force admin promotion/demotion through a SECURITY DEFINER RPC
--      that refuses self-edits and writes an audit log row.

create table if not exists public.admin_audit_log (
  id uuid default gen_random_uuid() primary key,
  actor_id uuid not null,
  target_id uuid not null,
  action text not null,
  details jsonb,
  created_at timestamptz default now()
);

alter table public.admin_audit_log enable row level security;

create policy "Admins read audit log" on public.admin_audit_log
  for select using (is_admin());

-- Trigger: refuse any UPDATE that flips protected admin fields unless the change is
-- explicitly sanctioned by set_user_admin() (which sets a session flag)
-- or comes from a database superuser running raw SQL (migrations,
-- bootstrap, the initial admin). Normal users can still update their
-- editable profile fields through the self-profile policy.
create or replace function public.guard_profile_admin_changes()
returns trigger
language plpgsql
as $$
begin
  if new.is_admin is distinct from old.is_admin then
    -- Allow if explicitly sanctioned by set_user_admin().
    if coalesce(current_setting('app.bypass_admin_guard', true), 'false') = 'true' then
      return new;
    end if;
    -- Allow superuser / migrations (Supabase SQL Editor).
    if current_user in ('postgres', 'supabase_admin') then
      return new;
    end if;
    raise exception 'is_admin can only be changed via public.set_user_admin()';
  end if;

  if new.is_disabled is distinct from old.is_disabled then
    -- Allow superuser / migrations (Supabase SQL Editor).
    if current_user in ('postgres', 'supabase_admin') then
      return new;
    end if;
    if not public.is_admin() then
      raise exception 'is_disabled can only be changed by an admin';
    end if;
  end if;

  return new;
end;
$$;

drop trigger if exists trg_guard_profile_admin_changes on public.profiles;
create trigger trg_guard_profile_admin_changes
  before update of is_admin, is_disabled on public.profiles
  for each row execute function public.guard_profile_admin_changes();

-- Sanctioned RPC for promoting/demoting admins. Refuses self-edits.
create or replace function public.set_user_admin(
  target_user_id uuid,
  make_admin boolean
) returns void
language plpgsql
security definer
set search_path = public
as $$
declare
  v_caller uuid := auth.uid();
  v_updated integer;
begin
  if v_caller is null then
    raise exception 'Authentication required';
  end if;
  if not public.is_admin() then
    raise exception 'Admin privileges required';
  end if;
  if v_caller = target_user_id then
    raise exception 'Admins cannot change their own is_admin flag';
  end if;

  -- Sanction the change for the trigger above, then immediately clear
  -- the flag so it can't leak into other statements in the same tx.
  perform set_config('app.bypass_admin_guard', 'true', true);
  update public.profiles set is_admin = make_admin where id = target_user_id;
  get diagnostics v_updated = row_count;
  perform set_config('app.bypass_admin_guard', 'false', true);

  if v_updated = 0 then
    raise exception 'Target user profile not found';
  end if;

  insert into public.admin_audit_log (actor_id, target_id, action, details)
  values (
    v_caller,
    target_user_id,
    case when make_admin then 'grant_admin' else 'revoke_admin' end,
    jsonb_build_object('make_admin', make_admin)
  );
end;
$$;

revoke all on function public.set_user_admin(uuid, boolean) from public;
grant execute on function public.set_user_admin(uuid, boolean) to authenticated;

-- ═══════════════════════════════════════════════
-- AI RATE LIMITING
-- ═══════════════════════════════════════════════
--
-- Replaces the in-memory rate limit in netlify/functions/ai.js, which
-- resets on every cold start and isn't shared across concurrent
-- function instances.
--
-- The Netlify function calls public.consume_ai_quota() with the user's
-- bearer token; the function reads auth.uid() server-side so callers
-- can't spoof another user. Limits are HARDCODED inside the function
-- so authenticated callers can't pass a higher cap.

create table if not exists public.ai_rate_limits (
  user_id uuid references auth.users on delete cascade primary key,
  window_start timestamptz not null default now(),
  count integer not null default 0
);

alter table public.ai_rate_limits enable row level security;
-- No CRUD policies — only the SECURITY DEFINER function below can
-- read or write this table from the REST API.

create or replace function public.consume_ai_quota()
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  v_user uuid := auth.uid();
  v_max constant integer := 10;        -- 10 requests
  v_window constant integer := 60;     -- per 60 seconds
  v_now timestamptz := now();
  v_count integer;
begin
  if v_user is null then
    return false;
  end if;

  insert into public.ai_rate_limits (user_id, window_start, count)
  values (v_user, v_now, 1)
  on conflict (user_id) do update
    set window_start = case
          when public.ai_rate_limits.window_start < v_now - make_interval(secs => v_window)
            then v_now
          else public.ai_rate_limits.window_start
        end,
        count = case
          when public.ai_rate_limits.window_start < v_now - make_interval(secs => v_window)
            then 1
          else public.ai_rate_limits.count + 1
        end
  returning count into v_count;

  return v_count <= v_max;
end;
$$;

revoke all on function public.consume_ai_quota() from public;
grant execute on function public.consume_ai_quota() to authenticated;

-- ═══════════════════════════════════════════════
-- PUBLIC PROFILE PAGES  (/u/:handle)
-- ═══════════════════════════════════════════════
--
-- Lets a learner opt in to a read-only public page
-- showing their display name, XP, streak, lesson
-- count, and badge count. Opt-in defaults to false;
-- everything else stays private.
--
-- Design: we DON'T add public RLS policies directly
-- to learner-owned tables like `progress`, because raw
-- rows include private lesson keys. Instead we create a
-- `public_profiles` VIEW that projects only aggregate
-- fields and grant SELECT on that view to `anon`.

alter table public.profiles
  add column if not exists is_public boolean default false;
alter table public.profiles
  add column if not exists public_handle text unique
    check (public_handle ~ '^[a-z0-9_-]{3,30}$');
-- ^ Enforce URL-safe lowercase handles: 3-30 chars, alphanumeric/dash/underscore only.

-- Case-insensitive handle lookup (so /u/Jenna and /u/jenna both work).
create index if not exists idx_profiles_public_handle_lower
  on public.profiles (lower(public_handle))
  where is_public = true;

create or replace view public.public_profiles
with (security_invoker = false) as
select
  p.id                    as id,
  p.display_name          as display_name,
  p.avatar_url            as avatar_url,
  p.public_handle         as handle,
  coalesce(x.total, 0)    as xp_total,
  coalesce(s.days, 0)     as streak_days,
  coalesce(lesson_counts.n, 0) as lessons_completed,
  coalesce(badge_counts.n, 0)  as badges_earned
from public.profiles p
left join public.xp x on x.user_id = p.id
left join public.streaks s on s.user_id = p.id
left join (
  select user_id, count(*)::int as n
  from public.progress
  group by user_id
) lesson_counts on lesson_counts.user_id = p.id
left join (
  select user_id, count(*)::int as n
  from public.badges
  group by user_id
) badge_counts on badge_counts.user_id = p.id
where p.is_public = true
  and coalesce(p.is_disabled, false) = false;

-- The view exposes aggregates only. Raw progress rows, lesson keys,
-- badge rows, XP rows, and streak rows are not an intentional public
-- API surface.
grant select on public.public_profiles to anon, authenticated;

-- Drop legacy public base-table policies so public profile pages stay
-- aggregate-only through the view above.
-- The following policies are intentionally not recreated.
drop policy if exists "Public profiles readable by anyone" on public.profiles;

drop policy if exists "Public xp readable" on public.xp;

drop policy if exists "Public streaks readable" on public.streaks;

drop policy if exists "Public progress count readable" on public.progress;

drop policy if exists "Public badges count readable" on public.badges;

revoke select on table public.profiles from anon;
revoke select on table public.xp from anon;
revoke select on table public.streaks from anon;
revoke select on table public.progress from anon;
revoke select on table public.badges from anon;

