import fs from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { COURSE_METADATA } from '../src/data/metadata.js';
import { HTML_QUIZZES } from '../src/data/html/quizzes.js';
import { CSS_QUIZZES } from '../src/data/css/quizzes.js';
import { JS_QUIZZES } from '../src/data/js/quizzes.js';
import { REACT_QUIZZES } from '../src/data/react/quizzes.js';
import { resolveQuizLessonId } from '../src/data/quizLessonIdResolver.js';
import { GLOSSARY } from '../src/data/reference/glossary.js';
import { buildSearchIndexFromCourses } from '../src/data/reference/search-index-core.js';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const projectRoot = path.resolve(__dirname, '..');

const COURSE_PAYLOADS = {
  html: { quizzes: HTML_QUIZZES },
  css: { quizzes: CSS_QUIZZES },
  js: { quizzes: JS_QUIZZES },
  react: { quizzes: REACT_QUIZZES },
};

export const searchManifestPath = path.join(
  projectRoot,
  'src',
  'data',
  'reference',
  'search-manifest.generated.js',
);

function buildScopedQuizKey(type, courseId, entityId) {
  if (!type || !courseId || !entityId) return '';
  return `${type}:${courseId}:${entityId}`;
}

async function loadCourseModules(courseId) {
  const courseFilePath = path.join(projectRoot, 'src', 'data', courseId, 'course.js');
  const courseSource = await fs.readFile(courseFilePath, 'utf8');
  const moduleImports = Array.from(
    courseSource.matchAll(/from\s+['"]\.\/modules\/([^'"]+\.json)['"]/g),
    (match) => match[1],
  );

  return Promise.all(
    moduleImports.map(async (relativeModulePath) => {
      const modulePath = path.join(projectRoot, 'src', 'data', courseId, 'modules', relativeModulePath);
      const moduleSource = await fs.readFile(modulePath, 'utf8');
      return JSON.parse(moduleSource);
    }),
  );
}

function buildQuizVariantIndex(courses) {
  const variantsByScope = new Map();

  function registerQuiz(scopeKey, quiz) {
    if (!scopeKey) return;

    const existingVariant = variantsByScope.get(scopeKey);
    if (!existingVariant) {
      variantsByScope.set(scopeKey, {
        primary: quiz,
        bonus: [],
      });
      return;
    }

    existingVariant.bonus.push(quiz);
  }

  courses.forEach((course) => {
    const lessonIds = new Set();
    const moduleIds = new Set();

    course.modules.forEach((module) => {
      moduleIds.add(module.id);
      module.lessons.forEach((lesson) => {
        lessonIds.add(lesson.id);
      });
    });

    const quizzes = COURSE_PAYLOADS[course.id]?.quizzes || [];
    quizzes.forEach((quiz) => {
      const lessonResolution = resolveQuizLessonId(course.id, quiz.lessonId, lessonIds);

      if (lessonResolution.resolvedLessonId) {
        registerQuiz(
          buildScopedQuizKey('l', course.id, lessonResolution.resolvedLessonId),
          quiz,
        );
      }

      if (quiz.moduleId && moduleIds.has(quiz.moduleId)) {
        registerQuiz(
          buildScopedQuizKey('m', course.id, quiz.moduleId),
          quiz,
        );
      }
    });
  });

  return variantsByScope;
}

export async function buildSearchManifestEntries() {
  const courses = await Promise.all(
    COURSE_METADATA.map(async (course) => ({
      ...course,
      modules: await loadCourseModules(course.id),
    })),
  );
  const quizVariantIndex = buildQuizVariantIndex(courses);

  return buildSearchIndexFromCourses(courses, GLOSSARY, {
    getQuizVariants(courseId, type, entityId) {
      return quizVariantIndex.get(buildScopedQuizKey(type, courseId, entityId)) || null;
    },
  }).map((entry) => ({
    title: entry.title,
    module: entry.module,
    course: entry.course,
    icon: entry.icon,
    keywords: entry.keywords,
    courseIdx: entry.courseIdx,
    modIdx: entry.modIdx,
    lesIdx: entry.lesIdx,
  }));
}

export async function buildSearchManifestSource() {
  const entries = await buildSearchManifestEntries();
  const payload = JSON.stringify(entries, null, 2);

  return `// Generated by scripts/generate-search-manifest.mjs.
// Do not edit by hand; regenerate after course or glossary changes.

export const SEARCH_INDEX_MANIFEST = Object.freeze(${payload});
`;
}

export function normalizeSearchManifestSource(source) {
  return typeof source === 'string' ? source.replace(/\r\n/g, '\n') : source;
}

export function searchManifestSourcesMatch(actualSource, expectedSource) {
  if (typeof actualSource !== 'string' || typeof expectedSource !== 'string') {
    return actualSource === expectedSource;
  }

  return normalizeSearchManifestSource(actualSource) === normalizeSearchManifestSource(expectedSource);
}

export async function writeSearchManifest() {
  const source = await buildSearchManifestSource();
  await fs.writeFile(searchManifestPath, source, 'utf8');
  return source;
}
